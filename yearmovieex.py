Movieyear=int(input("Enter Movie Year: "))
if Movieyear >=1990 and Movieyear <=1995:
     print("Golden Era")
elif Movieyear >=1996 and Movieyear <=2000:
     print("Sliver Era")
elif Movieyear >=2001 and Movieyear <=2021:
     print("21 century Movie")
elif Movieyear >=2022 and Movieyear <=2024:
     print("Current Era Movie")
else:
    print("Enter Correct Value")